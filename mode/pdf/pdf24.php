function adjustCalc($dev)
{
	$dev = ceil($dev);
	if ($dev >= 20 && $dev < 40) {
		$lat = (0.61 * $dev) / 30;
		$plus = ($dev - 30) / 2.1;
	} else
	if ($dev >= 40 && $dev < 50) {
		$lat = (0.9 * $dev) / 40;
		$plus = 0;
	} else
	if ($dev >= 50 && $dev < 60) {
		$lat = (1.11 * $dev) / 50;
		$plus = (($dev - 50) / 2.1) * -1;

		if ($plus < -4) {
			$plus *= 1.3;
		}
	} else
	if ($dev >= 60 && $dev < 70) {
		$lat = (1.21 * $dev) / 60;
		$plus = (($dev - 60) / 2.1) * -1;
		if ($plus < -3.8) {
			$plus *= 1.6;
		}
	}
	if ($dev >= 70 && $dev < 80) {
		$lat = (1.29 * $dev) / 70;
		$plus = (($dev - 70) / 2.1) * -1;
		if ($plus < -3.8) {
			$plus *= 1.9;
		}
	}
	if ($dev >= 80) {
		$lat = 1.35;
		$plus = 0;
	}
	return [$lat, $plus];
}


